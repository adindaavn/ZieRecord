    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreCategoryRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreCategoryRequest $request)
    {
        // dd ($request);
        Category::create($request->all()); //menyimpan data produk ke database
        return redirect('category')->with('success', 'Data Category berhasil ditambahkan!'); //redirect ke halaman produk dengan menampilkan alert
    }

    /**
